"""Scan node_modules for actually-installed known-bad packages."""
from __future__ import annotations

import json
from pathlib import Path

from scd.iocs.known_bad import IOCDatabase
from scd.models import (
    Confidence,
    Evidence,
    ExposureLevel,
    Finding,
    FindingCategory,
    Severity,
)
from scd.policies.loader import Policy


class NodeModulesScanner:
    """Walk node_modules to find installed malicious packages."""

    def __init__(
        self,
        target: Path,
        ioc_db: IOCDatabase,
        policy: Policy,
    ) -> None:
        self.target = target
        self.ioc_db = ioc_db
        self.policy = policy

    def scan(self) -> list[Finding]:
        findings: list[Finding] = []
        nm_dirs = self._find_node_modules()
        for nm_dir in nm_dirs:
            findings.extend(self._scan_node_modules(nm_dir))
        return findings

    def _find_node_modules(self) -> list[Path]:
        results = []
        # Direct child
        direct = self.target / "node_modules"
        if direct.is_dir():
            results.append(direct)
        # Workspace patterns
        for nm in self.target.rglob("node_modules"):
            # Skip deeply nested node_modules/X/node_modules
            parts = nm.relative_to(self.target).parts
            nm_count = sum(1 for p in parts if p == "node_modules")
            if nm_count <= 2 and nm not in results:
                results.append(nm)
        return results

    def _scan_node_modules(self, nm_dir: Path) -> list[Finding]:
        findings = []
        # Scan top-level packages
        for item in nm_dir.iterdir():
            if item.name.startswith("."):
                continue
            if item.name.startswith("@") and item.is_dir():
                # scoped packages
                for sub in item.iterdir():
                    name = f"{item.name}/{sub.name}"
                    findings.extend(self._check_installed_package(sub, name))
            elif item.is_dir():
                findings.extend(self._check_installed_package(item, item.name))
        return findings

    def _check_installed_package(self, pkg_dir: Path, name: str) -> list[Finding]:
        findings = []
        pkg_json = pkg_dir / "package.json"
        if not pkg_json.exists():
            return findings

        try:
            with open(pkg_json) as f:
                data = json.load(f)
        except (json.JSONDecodeError, OSError):
            return findings

        version = data.get("version", "")

        # Check known-bad exact match
        bad = self.ioc_db.is_known_bad(name, version)
        if bad:
            exposure = self._determine_exposure(pkg_dir, data)
            findings.append(Finding(
                severity=Severity.CRITICAL,
                category=FindingCategory.KNOWN_MALICIOUS,
                exposure_level=exposure,
                package_name=name,
                version=version,
                description=(
                    f"INSTALLED MALICIOUS PACKAGE: {name}@{version} found in node_modules. "
                    f"{bad.notes}. "
                    f"Exposure level: {exposure.value}."
                ),
                evidence=[
                    Evidence(source=str(pkg_dir), detail=f"Installed: {name}@{version}"),
                    Evidence(
                        source=str(pkg_json),
                        detail=f"Has postinstall: {self._has_postinstall(data)}",
                    ),
                ],
                confidence=Confidence.CONFIRMED,
                remediation=(
                    f"1. DELETE {pkg_dir} immediately.\n"
                    f"2. Remove from package.json and lockfile.\n"
                    f"3. This host is COMPROMISED — rotate ALL secrets.\n"
                    f"4. Preserve {pkg_dir} as forensic evidence before deletion.\n"
                    f"5. Check shell history and network logs."
                ),
            ))

        # Check policy blocklist
        blocked = self.policy.is_blocked(name, version)
        if blocked and not bad:
            findings.append(Finding(
                severity=Severity.HIGH,
                category=FindingCategory.POLICY_VIOLATION,
                exposure_level=ExposureLevel.INSTALLED,
                package_name=name,
                version=version,
                description=f"Policy-blocked package installed: {name}@{version}. {blocked.reason}",
                evidence=[Evidence(source=str(pkg_dir), detail=f"Installed: {name}@{version}")],
                confidence=Confidence.CONFIRMED,
                remediation=f"Remove {name} and run npm install with clean lockfile",
            ))

        # Check suspicious patterns
        sus = self.ioc_db.check_suspicious_pattern(name)
        if sus and not bad:
            findings.append(Finding(
                severity=Severity.MEDIUM,
                category=FindingCategory.SUSPICIOUS_PATTERN,
                exposure_level=ExposureLevel.INSTALLED,
                package_name=name,
                version=version,
                description=f"Suspicious package installed: {name} matches '{sus.pattern}'. {sus.reason}",
                evidence=[Evidence(source=str(pkg_dir), detail=f"Installed: {name}@{version}")],
                confidence=Confidence.MEDIUM,
                remediation=f"Verify {name} on npmjs.com",
            ))

        # Check for postinstall scripts only in packages that MATCH the indicator prefix.
        # indicator format: "plain-crypto-js/postinstall.js" — only check inside the matching package dir.
        for indicator in self.ioc_db.all_postinstall_indicators:
            # Extract the package name from the indicator path (first segment)
            indicator_parts = indicator.replace("\\", "/").split("/")
            indicator_pkg = indicator_parts[0]
            indicator_rel = "/".join(indicator_parts[1:]) if len(indicator_parts) > 1 else ""

            # Only check this indicator against the matching package
            if name.lower() != indicator_pkg.lower():
                continue

            check_path = pkg_dir / indicator_rel if indicator_rel else None
            if check_path and check_path.exists():
                findings.append(Finding(
                    severity=Severity.CRITICAL,
                    category=FindingCategory.KNOWN_MALICIOUS,
                    exposure_level=ExposureLevel.EXECUTED,
                    package_name=name,
                    version=version,
                    description=f"Malicious postinstall artifact found in {name}: {check_path}",
                    evidence=[Evidence(
                        source=str(check_path),
                        detail="Known malicious postinstall script or artifact",
                    )],
                    confidence=Confidence.CONFIRMED,
                    remediation="COMPROMISED. Preserve evidence. Rotate all secrets. Reimage host.",
                ))

        return findings

    def _determine_exposure(self, pkg_dir: Path, data: dict) -> ExposureLevel:
        """Determine if the package merely exists or if its code likely executed."""
        if self._has_postinstall(data):
            return ExposureLevel.EXECUTED
        # If the package is installed, its main code could have been imported
        return ExposureLevel.INSTALLED

    @staticmethod
    def _has_postinstall(data: dict) -> bool:
        scripts = data.get("scripts", {})
        return any(
            key in scripts
            for key in ["postinstall", "preinstall", "install", "prepare"]
        )
